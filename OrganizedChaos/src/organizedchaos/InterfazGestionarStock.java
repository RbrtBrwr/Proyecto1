    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String almacenModificar = cbGestionar.getSelectedItem().toString();
        for (int i = 0; i < menuInicial.miGrafo.warehouseList.getWarehouse(almacenModificar).items.size; i++) {
        //Este ciclo es para recorrer todos los Almacenes en el grafo e ir llenando las listas desplegables.
            cbLista.addItem(menuInicial.miGrafo.warehouseList.getWarehouse(almacenModificar).items.getNode(i).info.name);
        }
    }//GEN-LAST:event_jButton1MouseClicked

