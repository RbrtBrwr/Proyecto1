    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        String almacenModificar = cbGestionar.getSelectedItem().toString();
        String productoModificar = cbLista.getSelectedItem().toString();
        int cantidad = Integer.parseInt(Cantidad.getText().toString());
        menuInicial.miGrafo.warehouseList.getWarehouse(almacenModificar).items.buscarItem(productoModificar).quantity = cantidad;
        this.dispose();
    }//GEN-LAST:event_jButton2MouseClicked

